<script>
document.addEventListener("DOMContentLoaded", () => {
  const observer = new IntersectionObserver(entries => {
    entries.forEach(e => e.isIntersecting && e.target.classList.add("show"));
  }, { threshold: 0.15 });

  document.querySelectorAll(".fade-section").forEach(s => observer.observe(s));

  const links = document.querySelectorAll(".toc-link");
  const sections = [...document.querySelectorAll("section[id]")];
  const scrollBox = document.querySelector(".policy-scroll");

  links.forEach(link => {
    link.addEventListener("click", e => {
      e.preventDefault();
      const id = link.getAttribute("href").slice(1);
      const target = document.getElementById(id);
      scrollBox.scrollTo({
        top: target.offsetTop - 20,
        behavior: "smooth"
      });
    });
  });

  scrollBox.addEventListener("scroll", () => {
    let pos = scrollBox.scrollTop + 40;
    sections.forEach(sec => {
      if (pos >= sec.offsetTop && pos < sec.offsetTop + sec.offsetHeight) {
        links.forEach(l => l.classList.remove("active"));
        document.querySelector(`.toc-link[href="#${sec.id}"]`)?.classList.add("active");
      }
    });
  });

});
</script>

